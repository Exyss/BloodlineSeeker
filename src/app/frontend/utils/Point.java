package app.frontend.utils;

public class Point {
    private int x;
    private int y;
    
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    
    /** 
     * @return int
     */
    public int getX() {
        return this.x;
    }

    
    /** 
     * @return int
     */
    public int getY() {
        return this.y;
    }
}